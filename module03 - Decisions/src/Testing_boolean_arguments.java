
public class Testing_boolean_arguments {
	
	public static void main(String[] args) {
		boolean status = true;
		
		
		if (!status) {
			
			System.out.println("Hi");	//if Hi is printed, the if statement proceeded with a false boolean argument
		}
		else {
			System.out.println("Lisa");		//the if statement would not work with the false boolean argument
		}
		
	}
	

}
